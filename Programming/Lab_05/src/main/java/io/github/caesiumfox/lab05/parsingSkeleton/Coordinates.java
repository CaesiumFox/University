package io.github.caesiumfox.lab05.parsingSkeleton;

/**
 * Класс, содержащий первичные данные, полученные из файла json.
 * Подразумевается его использование как аргумент конструктора класса
 * {@link io.github.caesiumfox.lab05.element.Coordinates}.
 */
public class Coordinates {
    /**
     * Значение горизонтальной координаты
     */
    public float x;
    /**
     * Значение вертикальной координаты
     */
    public float y;
}
